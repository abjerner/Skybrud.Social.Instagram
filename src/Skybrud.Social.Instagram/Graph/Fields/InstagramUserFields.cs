namespace Skybrud.Social.Instagram.Graph.Fields {

    /// <summary>
    /// Static class with constants representing the fields of an Instagram user.
    /// </summary>
    /// <see>
    ///     <cref>https://developers.facebook.com/docs/instagram-api/reference/user/media#get-media</cref>
    /// </see>
    public static class InstagramUserFields {

        /// <summary>
        /// Represents the <c>biography</c> field.
        /// </summary>
        public const string Biography = "biography";

        /// <summary>
        /// Represents the <c>followers_count</c> field.
        /// </summary>
        public const string FollowersCount = "followers_count";

        /// <summary>
        /// Represents the <c>follows_count</c> field.
        /// </summary>
        public const string FollowsCount = "follows_count";

        /// <summary>
        /// Represents the <c>id</c> field.
        /// </summary>
        public const string Id = "id";

        /// <summary>
        /// Represents the <c>ig_id</c> field.
        /// </summary>
        public const string InstagramId = "ig_id";

        /// <summary>
        /// Represents the <c>media_count</c> field.
        /// </summary>
        public const string MediaCount = "media_count";

        /// <summary>
        /// Represents the <c>name</c> field.
        /// </summary>
        public const string Name = "name";

        /// <summary>
        /// Represents the <c>profile_picture_url</c> field.
        /// </summary>
        public const string ProfilePictureUrl = "profile_picture_url";

        /// <summary>
        /// Represents the <c>username</c> field.
        /// </summary>
        public const string Username = "username";

        /// <summary>
        /// Represents the <c>website</c> field.
        /// </summary>
        public const string Website = "website";

        /// <summary>
        /// Represents the <c>media</c> edge.
        /// </summary>
        public const string Media = "media";

    }

}